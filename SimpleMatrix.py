# Example graph with 4 vertices
adjacency_matrix = [
    [0, 1, 1, 0],  # Connections for vertex 0
    [1, 0, 1, 1],  # Connections for vertex 1
    [1, 1, 0, 1],  # Connections for vertex 2
    [0, 1, 1, 0]   # Connections for vertex 3
]

# Print adjacency matrix
for row in adjacency_matrix:
    print(row)
